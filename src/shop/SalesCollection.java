    void showData(String query){
        try {
            dbConnect();
            DefaultTableModel dtm=(DefaultTableModel)jTable1.getModel();
            dtm.setRowCount(0);
            double totalDisc=0;
            double totalAmount=0;
            double totalPaid=0;
            rs=st.executeQuery(query);
            for (double i=0; rs.next(); ) {
                double d;
                double t;
                double p;
                d=rs.getDouble(6);
                t=rs.getDouble(5);
                p=rs.getDouble(7);
                lbName.setText(rs.getString(2));
                i=rs.getDouble(8);
                totalDisc=totalDisc+d;
                totalAmount=totalAmount+t;
                totalPaid=totalPaid+p;
            dtm.addRow(new Object[]{rs.getString(1),rs.getString(3),rs.getString(5),rs.getString(6),rs.getString(7),rs.getString(8)});
            }
           Double totalDue1=totalAmount-totalPaid-totalDisc;
            txtTotalDue.setText(""+totalDue1);
            lbDiscount.setText(""+totalDisc);
            lbAmount.setText(""+totalAmount);
            lbPaid.setText(""+totalPaid);
        } catch (SQLException ex) {
            Logger.getLogger(SalesData.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
