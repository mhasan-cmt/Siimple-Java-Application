    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(!txtPaid.getText().isEmpty()){
        try {
            // Submit Paid:
            dbConnect();
            String paid1=txtPaid.getText();
            Double due1=Double.parseDouble(txtTotalDue.getText());
            double tDue=due1-Double.parseDouble(txtPaid.getText());
            String bill1=lastBill()+txtCustomer.getText();
            String sq="INSERT INTO `salescollection` VALUES('"+bill1+"','"+txtCustomer.getText()+"','"+lbDate.getText()+"','"+0+"','"+0+"','"+0+"','"+paid1+"','"+tDue+"')";
            int submit=st.executeUpdate(sq);
            if(submit>0){
                JOptionPane.showMessageDialog(this, "Submitted Successfully!!");
                String query="SELECT * FROM `salescollection` WHERE `Customer Name`='"+txtCustomer.getText()+"'";
                showData(query);   
            }
        } catch (SQLException ex) {
            Logger.getLogger(SalesCollection.class.getName()).log(Level.SEVERE, null, ex);
        }}
    }//GEN-LAST:event_jButton1ActionPerformed

